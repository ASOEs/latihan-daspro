# oprasi matematika

a = 20
b = 2

# oprasi pertambahan +
hasil = a + b
print(a,"+",b,"=", hasil)

# oprasi pengurangan -
hasil = a - b
print(a,"-",b,"=",hasil)

# oprasi perkalian
hasil = a * b
print(a,"*",b,"=",hasil)

# oprasi pembagian
hasil = a / b
print(a,"/",b,"=",hasil)

# oprasi eksponen dipangkatkan
hasil = a ** b
print(a,"**",b,"=",hasil)

# oprasi modulus %
hasil = a % b
print(a,"%",b,"=",hasil)

